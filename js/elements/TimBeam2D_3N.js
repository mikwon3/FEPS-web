/* ========================================================================
   TimBeam2D_3N.js  –  3절점 티모셴코 2D 보 요소 (TIMBEAM2D_3N)
   ──────────────────────────────────────────────────────────────────────
   학생 작성 요소 예제.

   ▶ 절점 배치
     ①────────③────────②
     nodes[0]  nodes[1]  nodes[2]
     (끝단1)  (중간절점)  (끝단2)

   ▶ 자유도: [u1,v1,θ1, u_mid,v_mid,θ_mid, u2,v2,θ2] (총 9 자유도)

   ▶ 구성방정식 (티모셴코 보)
     N = EA · ε           (축력-축변형률)
     M = EI · κ           (모멘트-곡률)
     V = κ_s · GA · γ     (전단력-전단변형률)

   ▶ 형상함수 (2차 라그랑지 — 아이소파라메트릭)
     N₁(ξ) = ξ(ξ−1)/2   (ξ=−1)
     N₂(ξ) = (1−ξ²)     (ξ= 0)
     N₃(ξ) = ξ(ξ+1)/2   (ξ=+1)
     모든 자유도 (u, v, θ) 에 동일한 형상함수 사용

   ▶ 전단 잠김 방지
     - 휨/축: 3-점 가우스 (완전 적분)
     - 전단: 2-점 가우스 (축소 적분)
     이를 통해 전단 잠김(shear locking) 없이 얇은 보도 정확히 해석 가능

   ▶ 전단보정계수: kappa 수정으로 조정
   ======================================================================== */

FepsElementRegistry.register({

    name:       'TIMBEAM2D_3N',
    category:   'beam2d_tim',
    nNodes:     3,
    dofPerNode: 3,

    // ── 전단보정계수 (κ_s) ────────────────────────────────────────────
    kappa: 5 / 6

    // ── 구현 설명 ─────────────────────────────────────────────────────
    // FepsElementCore.timoshenko3N_stif 에서:
    //   - 야코비안 J = L/2 (직선 등간격 요소)
    //   - 축 강성 Ka: 3-점 가우스로 ∫ EA·(dN/dx)² dx 계산
    //   - 휨 강성 Kb: 3-점 가우스로 ∫ EI·(dθ/dx)² dx 계산
    //   - 전단 강성 Ks: 2-점 가우스로 ∫ GAs·(dv/dx−θ)² dx 계산 (축소적분)
    // 학생은 kappa 값만 수정하면 됩니다.
});
