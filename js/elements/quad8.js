/* ========================================================================
   quad8.js  –  8절점 세렌디피티 사각형 요소 (QUAD8)
   ──────────────────────────────────────────────────────────────────────
   학생 작성 요소 예제.

   ▶ 절점 배치 (자연좌표 ξ,η ∈ [-1,1])
        4 ──7── 3
        │       │
        8       6
        │       │
        1 ──5── 2
     코너: 1=(−1,−1), 2=(+1,−1), 3=(+1,+1), 4=(−1,+1)
     중간: 5=(0,−1), 6=(+1,0), 7=(0,+1), 8=(−1,0)

   ▶ 형상함수 (세렌디피티 공식)
     코너 (ξ_i,η_i):
       N_i = (1/4)(1+ξ_i·ξ)(1+η_i·η)(ξ_i·ξ + η_i·η − 1)
     중간 (ξ=0 라인):
       N_5 = (1/2)(1−ξ²)(1−η)    [하단]
       N_6 = (1/2)(1+ξ)(1−η²)   [우측]
       N_7 = (1/2)(1−ξ²)(1+η)    [상단]
       N_8 = (1/2)(1−ξ)(1−η²)   [좌측]

   ▶ 구성방정식 선택: constitModel 변경
   ▶ 가우스 적분: 3×3
   ======================================================================== */

FepsElementRegistry.register({

    name:        'QUAD8',
    category:    'solid2d',
    nNodes:      8,
    dofPerNode:  2,
    cornerNodes: 4,

    constitModel: 'planeStress',   // 'planeStress' | 'planeStrain'
    gaussOrder:   3,

    // ── 형상함수 N(ξ, η) ─────────────────────────────────────────────
    shapeN(xi, eta) {
        const N = new Float64Array(8);
        // 코너 절점 (세렌디피티 공식)
        N[0] = 0.25 * (1 - xi) * (1 - eta) * (-xi - eta - 1);
        N[1] = 0.25 * (1 + xi) * (1 - eta) * ( xi - eta - 1);
        N[2] = 0.25 * (1 + xi) * (1 + eta) * ( xi + eta - 1);
        N[3] = 0.25 * (1 - xi) * (1 + eta) * (-xi + eta - 1);
        // 중간 절점 (각 변의 중점)
        N[4] = 0.5 * (1 - xi * xi) * (1 - eta);
        N[5] = 0.5 * (1 + xi)      * (1 - eta * eta);
        N[6] = 0.5 * (1 - xi * xi) * (1 + eta);
        N[7] = 0.5 * (1 - xi)      * (1 - eta * eta);
        return N;
    },

    // ── 형상함수 도함수 ∂N/∂ξ, ∂N/∂η ────────────────────────────────
    // 일반 공식 사용:
    //   코너(ξ_i,η_i): ∂N_i/∂ξ = (ξ_i/4)(1+η_i·η)(2ξ_i·ξ + η_i·η)
    //                  ∂N_i/∂η = (η_i/4)(1+ξ_i·ξ)(ξ_i·ξ + 2η_i·η)
    shapeDN(xi, eta) {
        const dxi  = new Float64Array(8);
        const deta = new Float64Array(8);

        // 코너 절점
        dxi[0]  =  0.25 * (1 - eta) * (2 * xi + eta);
        dxi[1]  =  0.25 * (1 - eta) * (2 * xi - eta);
        dxi[2]  =  0.25 * (1 + eta) * (2 * xi + eta);
        dxi[3]  =  0.25 * (1 + eta) * (2 * xi - eta);

        deta[0] =  0.25 * (1 - xi) * (xi + 2 * eta);
        deta[1] =  0.25 * (1 + xi) * (-xi + 2 * eta);
        deta[2] =  0.25 * (1 + xi) * (xi + 2 * eta);
        deta[3] =  0.25 * (1 - xi) * (-xi + 2 * eta);

        // 중간 절점
        dxi[4]  = -xi * (1 - eta);
        dxi[5]  =  0.5 * (1 - eta * eta);
        dxi[6]  = -xi * (1 + eta);
        dxi[7]  = -0.5 * (1 - eta * eta);

        deta[4] = -0.5 * (1 - xi * xi);
        deta[5] = -(1 + xi) * eta;
        deta[6] =  0.5 * (1 - xi * xi);
        deta[7] = -(1 - xi) * eta;

        return { dxi, deta };
    }
});
