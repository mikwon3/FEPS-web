/* ========================================================================
   bar2_3N.js  –  3절점 2D 봉(트러스) 요소 (BAR2_3N)
   ──────────────────────────────────────────────────────────────────────
   학생 작성 요소 예제.

   ▶ 절점 배치 (ξ ∈ [-1, +1])
     ①──────③──────②
     ξ=-1   ξ=0   ξ=+1
     nodes[0]  nodes[1]  nodes[2]
     (끝단1)  (중간절점)  (끝단2)

   ▶ 자유도: [u1,v1, u_mid,v_mid, u2,v2] (총 6 자유도)
     각 절점: x방향 변위 u, y방향 변위 v

   ▶ 형상함수 (2차 라그랑지)
     N₁(ξ) = ξ(ξ−1)/2    (끝단1)
     N₂(ξ) = (1−ξ²)      (중간)
     N₃(ξ) = ξ(ξ+1)/2    (끝단2)

   ▶ 구성방정식 (봉 요소)
     N = EA · ε   (축력-축변형률)
     (전단, 휨 무시 — 핀 접합 가정)

   ▶ 가우스 적분: 3점 (2차 형상함수에 충분)
   ======================================================================== */

FepsElementRegistry.register({

    name:       'BAR2_3N',
    category:   'bar1d',
    nNodes:     3,
    dofPerNode: 2,

    gaussOrder: 3,

    // ── 1D 형상함수 N(ξ) ─────────────────────────────────────────────
    // 절점 순서: nodes[0] → ξ=-1, nodes[1] → ξ=0, nodes[2] → ξ=+1
    shapeN1D(xi) {
        const N = new Float64Array(3);
        N[0] = xi * (xi - 1) / 2;   // ξ=-1 (끝단1)
        N[1] = (1 - xi * xi);        // ξ= 0 (중간)
        N[2] = xi * (xi + 1) / 2;   // ξ=+1 (끝단2)
        return N;
    },

    // ── 형상함수 도함수 dN/dξ ─────────────────────────────────────────
    shapeDN1D(xi) {
        const dN = new Float64Array(3);
        dN[0] = xi - 0.5;    // dN1/dξ = (2ξ-1)/2
        dN[1] = -2 * xi;     // dN2/dξ = -2ξ
        dN[2] = xi + 0.5;    // dN3/dξ = (2ξ+1)/2
        return dN;
    }

    // ── 구성방정식 ─────────────────────────────────────────────────
    // N = EA · ε  (축력-축변형률)
    // bar1d 카테고리에서는 FepsElementCore.bar1D_Stif 를 통해
    // 자동으로 EA 값을 적용합니다. 학생이 직접 수정할 필요 없습니다.
});
