/* ========================================================================
   trig6.js  –  6절점 2차 삼각형 요소 (TRIG6)
   ──────────────────────────────────────────────────────────────────────
   학생 작성 요소 예제.

   ▶ 절점 배치 (자연좌표 ξ,η, 기준삼각형: (0,0)-(1,0)-(0,1))
        3
        │╲
        6  5
        │    ╲
        1 ──4── 2
     코너: 1=(0,0), 2=(1,0), 3=(0,1)
     중간: 4=(0.5,0), 5=(0.5,0.5), 6=(0,0.5)

   ▶ 면적 좌표계
     L₁ = 1−ξ−η,  L₂ = ξ,  L₃ = η

   ▶ 형상함수 (2차 라그랑지)
     N₁ = L₁(2L₁−1)   N₂ = L₂(2L₂−1)   N₃ = L₃(2L₃−1)
     N₄ = 4L₁L₂        N₅ = 4L₂L₃        N₆ = 4L₁L₃

   ▶ 구성방정식: constitModel 변경
   ▶ 가우스 적분: 3점 삼각형 적분 (2차 다항식 완전 적분)
   ======================================================================== */

FepsElementRegistry.register({

    name:        'TRIG6',
    category:    'solid2d',
    nNodes:      6,
    dofPerNode:  2,
    cornerNodes: 3,     // 렌더링에 사용할 코너 절점 수
    triangular:  true,  // 삼각형 자연좌표계 (ξ,η ≥ 0, ξ+η ≤ 1)

    constitModel: 'planeStress',   // 'planeStress' | 'planeStrain'
    gaussOrder:   3,   // 삼각형 3-점 규칙

    // ── 형상함수 N(ξ, η) ─────────────────────────────────────────────
    // 면적 좌표: L1=1-ξ-η, L2=ξ, L3=η
    shapeN(xi, eta) {
        const L1 = 1 - xi - eta, L2 = xi, L3 = eta;
        const N = new Float64Array(6);
        N[0] = L1 * (2 * L1 - 1);     // 절점 1 (0,0)
        N[1] = L2 * (2 * L2 - 1);     // 절점 2 (1,0)
        N[2] = L3 * (2 * L3 - 1);     // 절점 3 (0,1)
        N[3] = 4 * L1 * L2;           // 절점 4 (0.5, 0)
        N[4] = 4 * L2 * L3;           // 절점 5 (0.5, 0.5)
        N[5] = 4 * L1 * L3;           // 절점 6 (0, 0.5)
        return N;
    },

    // ── 형상함수 도함수 ∂N/∂ξ, ∂N/∂η ────────────────────────────────
    // ∂L1/∂ξ = -1, ∂L1/∂η = -1
    // ∂L2/∂ξ = +1, ∂L2/∂η =  0
    // ∂L3/∂ξ =  0, ∂L3/∂η = +1
    shapeDN(xi, eta) {
        const L1 = 1 - xi - eta, L2 = xi, L3 = eta;
        const dxi  = new Float64Array(6);
        const deta = new Float64Array(6);

        // N1 = L1(2L1-1): ∂N1/∂ξ = (-1)(2L1-1) + L1·2·(-1) = -(2L1-1) - 2L1 = 1-4L1
        dxi[0]  = 1 - 4 * L1;
        deta[0] = 1 - 4 * L1;

        // N2 = L2(2L2-1): ∂N2/∂ξ = 4L2-1, ∂N2/∂η = 0
        dxi[1]  = 4 * L2 - 1;
        deta[1] = 0;

        // N3 = L3(2L3-1): ∂N3/∂ξ = 0, ∂N3/∂η = 4L3-1
        dxi[2]  = 0;
        deta[2] = 4 * L3 - 1;

        // N4 = 4L1L2: ∂N4/∂ξ = 4(-1)L2 + 4L1 = 4(L1-L2) = 4-8ξ-4η
        dxi[3]  = 4 * (L1 - L2);
        deta[3] = -4 * L2;

        // N5 = 4L2L3: ∂N5/∂ξ = 4L3, ∂N5/∂η = 4L2
        dxi[4]  = 4 * L3;
        deta[4] = 4 * L2;

        // N6 = 4L1L3: ∂N6/∂ξ = -4L3, ∂N6/∂η = 4(L1-L3) = 4-4ξ-8η
        dxi[5]  = -4 * L3;
        deta[5] = 4 * (L1 - L3);

        return { dxi, deta };
    }
});
