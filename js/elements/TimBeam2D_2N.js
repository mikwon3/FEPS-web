/* ========================================================================
   TimBeam2D_2N.js  –  2절점 티모셴코 2D 보 요소 (TIMBEAM2D_2N)
   ──────────────────────────────────────────────────────────────────────
   학생 작성 요소 예제.

   ▶ 절점 배치
     ①────────────②
     nodes[0]      nodes[1]
     (끝단1)       (끝단2)

   ▶ 자유도: [u1, v1, θ1, u2, v2, θ2] (총 6 자유도)
     u: 축 방향 변위, v: 횡방향 변위, θ: 단면 회전각

   ▶ 구성방정식 (티모셴코 보)
     N = EA · ε           (축력-축변형률)
     M = EI · κ           (모멘트-곡률)
     V = κ_s · GA · γ     (전단력-전단변형률)  ← 베르누이 보에 없는 항

   ▶ 전단보정계수 κ_s
     - 직사각형 단면: κ_s ≈ 5/6 ≈ 0.833
     - 원형 단면:     κ_s ≈ 0.9
     kappa 값을 수정하여 조정 가능

   ▶ 내부 구현
     FepsElementCore.timoshenko2N_stif 가 정확해 강성행렬을 계산합니다.
     전단유연성 파라미터: φ = 12EI/(κ_s·GA·L²)
     φ→0: 베르누이 보 수렴,  φ↑: 전단 유연성 증가
   ======================================================================== */

FepsElementRegistry.register({

    name:       'TIMBEAM2D_2N',
    category:   'beam2d_tim',
    nNodes:     2,
    dofPerNode: 3,

    // ── 전단보정계수 (κ_s) ────────────────────────────────────────────
    // 직사각형 단면 기본값: 5/6
    // 필요에 따라 0.85, 0.9 등으로 변경 가능
    kappa: 5 / 6

    // ── 구성방정식 요약 ───────────────────────────────────────────────
    // N = EA · ε           ← 재료 prop.A, mat.E 에서 EA 자동 계산
    // M = EI · κ           ← 재료 prop.Iz, mat.E 에서 EI 자동 계산
    // V = κ_s · GA · γ    ← kappa, mat.E, mat.nu 에서 G = E/(2(1+ν)),
    //                         GAs = G · κ_s · A 자동 계산
    //
    // 강성행렬은 FepsElementCore.timoshenko2N_stif 에서 정확해로 계산됩니다.
    // 학생은 kappa 값만 수정하면 됩니다.
});
